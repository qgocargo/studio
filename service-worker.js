// A basic service worker for PWA capabilities

const CACHE_NAME = 'qgocargo-cache-v1';
const urlsToCache = [
  '/',
  '/index.html',
  '/track.html',
  'https://cdn.tailwindcss.com',
  'https://fonts.googleapis.com/css2?family=Inter:wght@400;500;600;700;800&display=swap',
  '/assets/logo.png',
  '/assets/logo-192.png',
  '/assets/logo-512.png',
  // Note: JS modules and Firebase SDKs are loaded via network and not cached here.
  // This is a simple cache-first strategy. For offline data, the app uses Firestore's offline persistence.
];

self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(cache => {
        console.log('Opened cache');
        return cache.addAll(urlsToCache);
      })
  );
});

self.addEventListener('fetch', event => {
  event.respondWith(
    caches.match(event.request)
      .then(response => {
        if (response) {
          return response; // Serve from cache
        }
        return fetch(event.request); // Fetch from network
      }
    )
  );
});

self.addEventListener('activate', event => {
  const cacheWhitelist = [CACHE_NAME];
  event.waitUntil(
    caches.keys().then(cacheNames => {
      return Promise.all(
        cacheNames.map(cacheName => {
          if (cacheWhitelist.indexOf(cacheName) === -1) {
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
});
